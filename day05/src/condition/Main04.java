package condition;

public class Main04 {

	public static void main(String[] args) {
		boolean korean = true;
		
		if( korean == true ) {
			System.out.println("한국사람입니다.");
		}
		
		if( korean ) {
			System.out.println("한국사람입니다.");
		}
		
		if( !korean ) {
			System.out.println("한국사람입니다.");
		}
	}

}
